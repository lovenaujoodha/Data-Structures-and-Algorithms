    public static void bubbleSort(String[] a) {
        int count = 0;
        for (int i = 0; i < a.length; i++) {
            for (int j = 1; j < a.length - i; j++) {
                if (a[j - 1].compareTo(a[j]) > 0) {
                    swap(a, j - 1, j);
                }
            }
        }
        count = count + 1;
        System.out.println(count);
    }

    private static void swap(String[] a, int i, int j) {
        if (i == j) {
            return;
        }

        String temp = a[j];
        a[j] = a[i];
        a[i] = temp;
    }

